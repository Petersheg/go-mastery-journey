package greetings06

import (
	"testing"
	"regexp"
)

// TestHelloName calls greetings.Hello with a name, checking for a valid return value.
func TestHelloName(t *testing.T) {
	name := "Peter"
	want := regexp.MustCompile(`\b`+name+`\b`)

	message, error := Hello("Gladys")

	//If message do not match or there is error
	if(!want.MatchString(message) || error != nil) {
		t.Errorf(`Hello("Gladys") = %q, %v, want match for %#q, nil`, message, error, want)
	}
}

// TestHelloEmpty calls greetings.Hello with an empty string, checking for an error.
func TestHelloEmpty(t *testing.T) {
	msg, err := Hello("")

	if(msg != "" || err == nil) {
		t.Errorf(`Hello("") = %q , %v, want "", error`, msg, err )
	}
}